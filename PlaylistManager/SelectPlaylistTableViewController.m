#import "SelectPlaylistTableViewController.h"
#import "SpotifyRetriever.h"

@interface SelectPlaylistTableViewController ()

@property NSArray* userPlaylists;

@end

@implementation SelectPlaylistTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    [self loadUserPlaylists];
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
}

-(void) loadUserPlaylists {
    [SPTRequest playlistsForUserInSession:[SpotifyRetriever instance].session callback:^(NSError *error, id playlists) {
        if (error != nil){
            NSLog(@"*** URI request error: %@", error);
            return;
        }
        self.userPlaylists = [playlists items];
    }];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    
    // Return the number of rows in the section.
    return [self.userPlaylists count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"playlistListCell" forIndexPath:indexPath];
    SPTPartialPlaylist* p = [self.userPlaylists objectAtIndex:indexPath.row];
    cell.textLabel.text = [NSString stringWithFormat:@"%@ - %lud tracks]", [p name], [p trackCount]];
    return cell;
}

